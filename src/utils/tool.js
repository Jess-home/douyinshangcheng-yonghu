//  获取客户端系统
export function getOS() {
  const userAgent = navigator.userAgent,
    isWindowsPhone = /(?:Windows Phone)/.test(userAgent),
    isSymbian = /(?:SymbianOS)/.test(userAgent) || isWindowsPhone,
    isAndroid = /(?:Android)/.test(userAgent),
    isFireFox = /(?:Firefox)/.test(userAgent),
    isChrome = /(?:Chrome|CriOS)/.test(userAgent),
    isTablet =
      /(?:iPad|PlayBook)/.test(userAgent) ||
      (isAndroid && !/(?:Mobile)/.test(userAgent)) ||
      (isFireFox && /(?:Tablet)/.test(userAgent)),
    isPhone = /(?:iPhone)/.test(userAgent) || isWindowsPhone,
    isPc = !isPhone && !isAndroid && !isTablet

  if (isPhone) {
    return 'Phone'
  }
  if (isAndroid) {
    return 'Android'
  }
  if (isTablet) {
    return 'Tablet'
  }
  if (isPc) {
    return 'PC'
  }
  return 'Unknow'
}

export function isAppleSafari() {
  return (
    /AppleWebKit/.test(navigator.userAgent) &&
    /Safari/.test(navigator.userAgent) &&
    !/Chrome/.test(navigator.userAgent)
  )
}

//  数组切割
export function splitArray(array, size) {
  const result = []
  for (let i = 0; i < array.length; i += size) {
    result.push(array.slice(i, i + size))
  }
  return result
}

//  路由白名单判断
export function isContainWhite(whites, path) {
  const t = whites.find((item) => {
    return path.includes(item)
  })
  return !!t
}
